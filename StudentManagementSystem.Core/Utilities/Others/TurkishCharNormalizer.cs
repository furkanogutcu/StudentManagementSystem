namespace StudentManagementSystem.Core.Utilities.Others
{
    public static class TurkishCharNormalizer
    {
        //https://stackoverflow.com/questions/13660761/how-to-convert-turkish-chars-to-english-chars-in-a-string
        public static string Normalization(string text)
        {
            char[] turkishChars = { 'ı', 'ğ', 'İ', 'Ğ', 'ç', 'Ç', 'ş', 'Ş', 'ö', 'Ö', 'ü', 'Ü' };
            char[] englishChars = { 'i', 'g', 'I', 'G', 'c', 'C', 's', 'S', 'o', 'O', 'u', 'U' };

            // Match chars
            for (int i = 0; i < turkishChars.Length; i++)
                text = text.Replace(turkishChars[i], englishChars[i]);

            return text;
        }
    }
}
